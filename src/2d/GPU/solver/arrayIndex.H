#ifndef __ARRAYINDEX_H__
#define __ARRAYINDEX_H__

#define GET_INDEX_SHARED_SPEED_0INDEX(ROW,COL,WAVE,NUMWAVES,BLOCKHEIGHT,BLOCKWIDTH) \
    (COL+\
     BLOCKWIDTH*(ROW+WAVE*BLOCKHEIGHT))

#define GET_INDEX_SHARED_SPEED_1INDEX(ROW,COL,WAVE,NUMWAVES,BLOCKHEIGHT,BLOCKWIDTH) \
    (COL+\
     BLOCKWIDTH*(ROW-1+(WAVE-1)*BLOCKHEIGHT))

#define GET_INDEX_SHARED_WAVE_0INDEX(ROW,COL,WAVE,STATE,NUMWAVES,NUMSTATES,BLOCKHEIGHT,BLOCKWIDTH) \
    (COL+\
     BLOCKWIDTH*(\
         STATE+\
         NUMSTATES*(\
             WAVE+\
             NUMWAVES*ROW)\
         )\
    )

#define GET_INDEX_SHARED_WAVE_1INDEX(ROW,COL,WAVE,STATE,NUMWAVES,NUMSTATES,BLOCKHEIGHT,BLOCKWIDTH) \
    (COL+\
     BLOCKWIDTH*(\
         STATE-1+\
         NUMSTATES*(\
             WAVE-1+\
             NUMWAVES*(ROW-1))\
         )\
    )

#endif
